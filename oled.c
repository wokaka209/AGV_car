//反显函数
void OLED_ColorTurn(u8 i)
{
	if(i==0)
		{
			OLED_WR_Byte(0xA6,OLED_CMD);//正常显示
		}
	if(i==1)
		{
			OLED_WR_Byte(0xA7,OLED_CMD);//反色显示
		}
}

void OLED_DisplayTurn(u8 i)
{
	if(i==0)
		{
			OLED_WR_Byte(0xC8,OLED_CMD);//正常显示
			OLED_WR_Byte(0xA1,OLED_CMD);
		}
	if(i==1)
		{
			OLED_WR_Byte(0xC0,OLED_CMD);//反转显示
			OLED_WR_Byte(0xA0,OLED_CMD);
		}
}
//***********开始
void I2C_start(void)
{
	OLED_SDA_hig();
	delay(2);
	OLED_SCL_hig();
	delay(2);
	OLED_SDA_low();
	delay(2);
	OLED_SCL_low();
	delay(2);
}

//***********结束
void I2C_stop(void)
{
	OLED_SCL_hig();
	delay(2);
	OLED_SDA_low();
	delay(2);
	OLED_SDA_hig();
	delay(2);
}

//***********等待信号响应
void I2C_WaitAck(void) //测数据信号的电平
{
	OLED_SCL_hig();
	delay(2);
	OLED_SCL_low();
	delay(2);
}

//************写入一个字节
void Send_Byte(u8 dat)
{
	u8 i;
	for (i = 0; i < 8; i++)
	{
		OLED_SCL_low();
		delay(2);
		if(dat&0x80)
		{
			OLED_SDA_hig();
			delay(2);
		}
		else 
		{
			OLED_SDA_low();
			delay(2);
		}
		OLED_SCL_hig();
		delay(5);
		OLED_SCL_low();
		delay(2);
		dat<<=1;
	}
}


//发送一个字节
//向SSD1306写入一个字节。
//mode:数据/命令标志 0,表示命令;1,表示数据;
void OLED_WR_Byte(u8 dat,u8 mode)
{
	I2C_start();
	Send_Byte(0x78);
	I2C_WaitAck();
	if(mode){Send_Byte(0x40);}
  else{Send_Byte(0x00);}
	I2C_WaitAck();
	Send_Byte(dat);
	I2C_WaitAck();
	I2C_stop();
}


